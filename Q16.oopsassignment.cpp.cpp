#include <iostream>
using namespace std;

int main(){
    cout<<" ---demonstration of increment and decrement operators--- "<<endl;
    int x = 10;
    cout<<"Initial value of x: "<<x<<endl;
    cout<<"Post-increment x: "<<x++<<endl; 
    cout<<"After post-increment, x: "<<x<<endl; 
    cout<<"Pre-increment x: "<<++x<<endl; 
    cout<<"After pre-increment, x: "<<x<<endl; 
    cout<<"Post-decrement x: "<<x--<<endl; 
    cout<<"After post-decrement, x: "<<x<<endl; 
    cout<<"Pre-decrement x: "<<--x<<endl; 
    cout<<"After pre-decrement, x: "<<x<<endl; 
    return 0;
}