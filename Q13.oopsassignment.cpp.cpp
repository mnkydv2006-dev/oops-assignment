#include <iostream>
using namespace std;
int main(){

    cout<<":-logical operators demonstration-:"<<endl;
    bool p = true;
    bool q = false;
    cout<<"p = "<<p<<", q = "<<q<<endl;
    cout<<"p && q = "<<(p && q)<<endl; 
    cout<<"p || q = "<<(p || q)<<endl; 
    cout<<"!p = "<<(!p)<<endl;       
    cout<<"!q = "<<(!q)<<endl;         
    return 0;
}